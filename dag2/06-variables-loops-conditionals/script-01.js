/*

Elke programmeertaal heeft variabelen. Je kan dit zien als een doosje 
waar je 1 ding tegelijk in kan stoppen. Wat je erin stopt, en hoe je 
het noemt mag je helemaal zelf weten.

In JavaScript beginnen variabelen met let, const, of var. Voor nu werken
we alleen met 'let'. 

Een variabele ziet er bijvoorbeeld zo uit:

*/
let naam = 'Hoi';

/* 

Je hebt verschillende soorten variabelen. Het type hierboven noemen we een string, dit is een
stukje tekst. Naast strings heb je ook nummers, integers genaamd. Ook zijn er booleans, deze 
geven aan of iets waar is of niet.

*/
let nummer = 1; // integer
let hetIsDinsdag = true; // boolean

/*

Er zijn ook nog andere soorten waar we later op terug komen. Voor nu gaan we een variabele
definieren naar het scherm schrijven.

*/